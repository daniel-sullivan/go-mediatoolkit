// populateHuffmanCodeWords wires the ht[t].table code-word members (and the
// emitter-only ht[t].hlen members the count slice left nil) so the Huffman
// EMITTERS — Huffmancode (bitstream.c:560) and huffman_coder_count1
// (bitstream.c:491) — find the code words and lengths for every selectable
// table. It mirrors the .table / .hlen columns of tables.c:409's ht[HTN]
// initializer for the tables an encoder can select (0..33); the xlen / linmax
// geometry and the count-slice hlen subset are set by populateHuffmanLengths.
//
// The count slice (count_bit_*) reads ht[].hlen only for tables 1,7..15; those
// were already filled, and the emitter shares the same arrays. The remaining
// hlen sets here (2,3,5,6,16..33) are read only by the emitters, so adding them
// does not perturb the bit-count path. huffman_init calls this once, right
// after populateHuffmanLengths.
func populateHuffmanCodeWords() {
	// tables.c:409 ht[HTN] .table / .hlen columns (the .table NULLs at indices
	// 0, 4 and 14 stay nil — those tables are never coded).
	ht[1].Table, ht[1].Hlen = t1HB, t1l
	ht[2].Table, ht[2].Hlen = t2HB, t2l
	ht[3].Table, ht[3].Hlen = t3HB, t3l
	ht[5].Table, ht[5].Hlen = t5HB, t5l
	ht[6].Table, ht[6].Hlen = t6HB, t6l
	ht[7].Table, ht[7].Hlen = t7HB, t7l
	ht[8].Table, ht[8].Hlen = t8HB, t8l
	ht[9].Table, ht[9].Hlen = t9HB, t9l
	ht[10].Table, ht[10].Hlen = t10HB, t10l
	ht[11].Table, ht[11].Hlen = t11HB, t11l
	ht[12].Table, ht[12].Hlen = t12HB, t12l
	ht[13].Table, ht[13].Hlen = t13HB, t13l
	ht[15].Table, ht[15].Hlen = t15HB, t15l

	// 16..23: ESC-16 family — all share t16HB / t16l (xlen / linmax differ and
	// were set by populateHuffmanLengths; the emitter uses a fixed xlen of 16
	// for the .table / .hlen index and reads ht[].xlen only as linbits).
	for i := 16; i <= 23; i++ {
		ht[i].Table, ht[i].Hlen = t16HB, t16l
	}
	// 24..31: ESC-24 family — all share t24HB / t24l.
	for i := 24; i <= 31; i++ {
		ht[i].Table, ht[i].Hlen = t24HB, t24l
	}

	// 32..33: count1 quadruple codebooks.
	ht[32].Table, ht[32].Hlen = t32HB, t32lHB
	ht[33].Table, ht[33].Hlen = t33HB, t33lHB
}
