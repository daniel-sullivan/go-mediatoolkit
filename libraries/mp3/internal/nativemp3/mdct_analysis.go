// # Scope of this slice ("mdct-analysis")
//
// This slice covers LAME's polyphase analysis filterbank and the MDCT that
// turns 576 PCM samples per granule into 32 subbands x 18 MDCT lines. The
// ported functions are window_subband / mdct_short / mdct_long / mdct_sub48
// (newmdct.c) — the 32-band polyphase analysis filter, the 6-line short-block
// MDCT, the 18-line long-block MDCT, and the mdct_sub48 driver that windows
// two channels' worth of PCM, runs the filterbank, then the per-band MDCT, and
// finally the long-block aliasing-reduction butterfly.
//
// The companion FFT/FHT (fft.c) the psy model squares into energies is a
// SEPARATE area, already translated in fft.go (fht / fftShort / fftLong /
// initFFT). The shared block-type constants (NormType/ShortType/...), SBLIMIT,
// and the sqrt2Const/piConst values live in psymodel.go and are reused here
// rather than redeclared.
//
// # Sample / data layout
//
// All transform math is single-precision (C `FLOAT` == `float32`; C
// `sample_t` == `float32`). PCM enters mdct_sub48 as a flat float32 run with
// the same 286-sample look-ahead offset LAME uses (`wk = w0 + 286`). The
// filterbank writes 18 subband-sample frames of 32 bands each into
// EncStateVar.SbSample; mdct_sub48 then emits 32*18 = 576 MDCT lines into
// GrInfo.Xr.
//
// # Strict mode (kind=fp)
//
// Every function here is floating-point. Go's arm64 backend fuses `a*b+c`
// into a single-rounded FMA, which diverges from clang's separately-rounded
// `-ffp-contract=off` build of the C oracle in the last ULP. So the strict
// (mp3_strict) build routes every float32 multiply and add/sub through the
// //go:noinline helpers in mdct_analysis_fp_strict.go, exactly as the
// huffman dequant slice does, making the products opaque to the SSA fuser.
// The default build (mdct_analysis_fp_default.go) inlines the plain
// operators and may fuse/vectorize; it is within PSNR noise but is NOT a
// bit-exact target.
//
// Every ported function carries a doc comment naming its newmdct.c counterpart
// as file:line so a future reader can diff against the C. The package is
// internal to libraries/mp3; external code goes through
// libraries/mp3.NewNativeEncoder.
package nativemp3

