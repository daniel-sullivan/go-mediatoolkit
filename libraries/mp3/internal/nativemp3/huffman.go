// L3Pow43 returns x^(4/3) (with x interpreted unsigned) for the Huffman
// linbits-escape path, mirroring minimp3's L3_pow_43 (minimp3.h:707).
//
//	static float L3_pow_43(int x)
//	{
//	    float frac;
//	    int sign, mult = 256;
//	    if (x < 129)
//	        return g_pow43[16 + x];
//	    if (x < 1024) { mult = 16; x <<= 3; }
//	    sign = 2*x & 64;
//	    frac = (float)((x & 63) - sign) / ((x & ~63) + sign);
//	    return g_pow43[16 + ((x + sign) >> 6)]*(1.f + frac*((4.f/3) + frac*(2.f/9)))*mult;
//	}
//
// The small-x branch is an exact table read; the large-x branch interpolates
// in the g_pow43 lookup. The polynomial 1 + frac*(4/3 + frac*2/9) and the two
// trailing multiplies route through the f32* helpers so the mp3_strict build
// keeps the same separately-rounded float32 operation order as the oracle.
func L3Pow43(x int) float32 {
	mult := 256

	if x < 129 {
		return gPow43[16+x]
	}

	if x < 1024 {
		mult = 16
		x <<= 3
	}

	sign := 2 * x & 64
	frac := f32div(float32((x&63)-sign), float32((x&^63)+sign))
	// 1.f + frac*((4.f/3) + frac*(2.f/9))
	poly := f32add(1.0, f32mul(frac, f32add(4.0/3, f32mul(frac, 2.0/9))))
	return f32mul(f32mul(gPow43[16+((x+sign)>>6)], poly), float32(mult))
}

// L3Huffman unpacks one granule's big-value and count1 regions from the bit
// reader into dst (576 frequency lines), applying the scalefactor-driven
// dequantization, mirroring minimp3's L3_huffman (minimp3.h:728).
//
// dst is the granule output buffer; bs is positioned at the start of this
// granule's Huffman data; grInfo supplies the region codebook selection and
// scalefactor-band widths; scf is the per-band dequantization gain table;
// layer3grLimit is the absolute bit position at which this granule's data
// ends (used to stop the count1 loop). On return bs.Pos is set to
// layer3grLimit so the next granule starts at the correct bit.
//
// The reader runs over a private 32-bit window (bsCache) rather than the
// shared get_bits path, exactly as the C does, peeking and flushing bits
// through the local PEEK_BITS / FLUSH_BITS / CHECK_BITS macros (translated
// inline below).
func L3Huffman(dst []float32, bs *BitStream, grInfo *L3GrInfo, scf []float32, layer3grLimit int) {
	// PEEK_BITS(n)  -> bsCache >> (32 - n)
	// FLUSH_BITS(n) -> bsCache <<= n; bsSh += n
	// CHECK_BITS    -> while bsSh >= 0 { bsCache |= buf[bsNextPtr++] << bsSh; bsSh -= 8 }
	// BSPOS         -> (bsNextPtr - bsBufBase)*8 - 24 + bsSh
	// These are open-coded at each use site to match the C macro expansion.

	var one float32 = 0.0
	ireg := 0
	bigValCnt := int(grInfo.BigValues)
	sfb := grInfo.Sfbtab // walked via sfb = sfb[1:] in place of *sfb++
	sfbIdx := 0
	scfIdx := 0
	dstIdx := 0

	buf := bs.Buf
	bsBufBase := 0          // index in buf corresponding to bs->buf
	bsNextPtr := bs.Pos / 8 // const uint8_t *bs_next_ptr = bs->buf + bs->pos/8;
	bsCache := (((uint32(buf[bsNextPtr])*256+uint32(buf[bsNextPtr+1]))*256+uint32(buf[bsNextPtr+2]))*256 + uint32(buf[bsNextPtr+3])) << uint(bs.Pos&7)
	bsSh := (bs.Pos & 7) - 8
	bsNextPtr += 4

	for bigValCnt > 0 {
		tabNum := int(grInfo.TableSelect[ireg])
		sfbCnt := int(grInfo.RegionCount[ireg])
		ireg++
		codebook := int(tabindex[tabNum]) // offset into l3Tabs
		linbits := int(gLinbits[tabNum])
		if linbits != 0 {
			for {
				np := int(sfb[sfbIdx]) / 2
				sfbIdx++
				pairsToDecode := minimp3Min(bigValCnt, np)
				one = scf[scfIdx]
				scfIdx++
				for {
					w := 5
					leaf := int(l3Tabs[codebook+int(bsCache>>(32-uint(w)))])
					for leaf < 0 {
						// FLUSH_BITS(w)
						bsCache <<= uint(w)
						bsSh += w
						w = leaf & 7
						leaf = int(l3Tabs[codebook+int(bsCache>>(32-uint(w)))-(leaf>>3)])
					}
					// FLUSH_BITS(leaf >> 8)
					bsCache <<= uint(leaf >> 8)
					bsSh += leaf >> 8

					for j := 0; j < 2; j++ {
						lsb := leaf & 0x0F
						if lsb == 15 {
							lsb += int(bsCache >> (32 - uint(linbits)))
							// FLUSH_BITS(linbits)
							bsCache <<= uint(linbits)
							bsSh += linbits
							// CHECK_BITS
							for bsSh >= 0 {
								bsCache |= uint32(buf[bsNextPtr]) << uint(bsSh)
								bsNextPtr++
								bsSh -= 8
							}
							sign := float32(1)
							if int32(bsCache) < 0 {
								sign = -1
							}
							dst[dstIdx] = f32mul(f32mul(one, L3Pow43(lsb)), sign)
						} else {
							dst[dstIdx] = f32mul(gPow43[16+lsb-16*int(bsCache>>31)], one)
						}
						// FLUSH_BITS(lsb ? 1 : 0)
						if lsb != 0 {
							bsCache <<= 1
							bsSh++
						}
						dstIdx++
						leaf >>= 4
					}
					// CHECK_BITS
					for bsSh >= 0 {
						bsCache |= uint32(buf[bsNextPtr]) << uint(bsSh)
						bsNextPtr++
						bsSh -= 8
					}
					pairsToDecode--
					if pairsToDecode == 0 {
						break
					}
				}
				bigValCnt -= np
				sfbCnt--
				if !(bigValCnt > 0 && sfbCnt >= 0) {
					break
				}
			}
		} else {
			for {
				np := int(sfb[sfbIdx]) / 2
				sfbIdx++
				pairsToDecode := minimp3Min(bigValCnt, np)
				one = scf[scfIdx]
				scfIdx++
				for {
					w := 5
					leaf := int(l3Tabs[codebook+int(bsCache>>(32-uint(w)))])
					for leaf < 0 {
						// FLUSH_BITS(w)
						bsCache <<= uint(w)
						bsSh += w
						w = leaf & 7
						leaf = int(l3Tabs[codebook+int(bsCache>>(32-uint(w)))-(leaf>>3)])
					}
					// FLUSH_BITS(leaf >> 8)
					bsCache <<= uint(leaf >> 8)
					bsSh += leaf >> 8

					for j := 0; j < 2; j++ {
						lsb := leaf & 0x0F
						dst[dstIdx] = f32mul(gPow43[16+lsb-16*int(bsCache>>31)], one)
						// FLUSH_BITS(lsb ? 1 : 0)
						if lsb != 0 {
							bsCache <<= 1
							bsSh++
						}
						dstIdx++
						leaf >>= 4
					}
					// CHECK_BITS
					for bsSh >= 0 {
						bsCache |= uint32(buf[bsNextPtr]) << uint(bsSh)
						bsNextPtr++
						bsSh -= 8
					}
					pairsToDecode--
					if pairsToDecode == 0 {
						break
					}
				}
				bigValCnt -= np
				sfbCnt--
				if !(bigValCnt > 0 && sfbCnt >= 0) {
					break
				}
			}
		}
	}

	// for (np = 1 - big_val_cnt;; dst += 4)
	np := 1 - bigValCnt
	for {
		var codebookCount1 []uint8
		if grInfo.Count1Table != 0 {
			codebookCount1 = tab33[:]
		} else {
			codebookCount1 = tab32[:]
		}
		leaf := int(codebookCount1[bsCache>>(32-4)])
		if leaf&8 == 0 {
			leaf = int(codebookCount1[(leaf>>3)+int(bsCache<<4>>(32-uint(leaf&3)))])
		}
		// FLUSH_BITS(leaf & 7)
		bsCache <<= uint(leaf & 7)
		bsSh += leaf & 7
		// BSPOS > layer3gr_limit
		if (bsNextPtr-bsBufBase)*8-24+bsSh > layer3grLimit {
			break
		}

		// RELOAD_SCALEFACTOR (first)
		np--
		if np == 0 {
			np = int(sfb[sfbIdx]) / 2
			sfbIdx++
			if np == 0 {
				break
			}
			one = scf[scfIdx]
			scfIdx++
		}
		// DEQ_COUNT1(0)
		if leaf&(128>>0) != 0 {
			if int32(bsCache) < 0 {
				dst[dstIdx+0] = -one
			} else {
				dst[dstIdx+0] = one
			}
			bsCache <<= 1
			bsSh++
		}
		// DEQ_COUNT1(1)
		if leaf&(128>>1) != 0 {
			if int32(bsCache) < 0 {
				dst[dstIdx+1] = -one
			} else {
				dst[dstIdx+1] = one
			}
			bsCache <<= 1
			bsSh++
		}
		// RELOAD_SCALEFACTOR (second)
		np--
		if np == 0 {
			np = int(sfb[sfbIdx]) / 2
			sfbIdx++
			if np == 0 {
				break
			}
			one = scf[scfIdx]
			scfIdx++
		}
		// DEQ_COUNT1(2)
		if leaf&(128>>2) != 0 {
			if int32(bsCache) < 0 {
				dst[dstIdx+2] = -one
			} else {
				dst[dstIdx+2] = one
			}
			bsCache <<= 1
			bsSh++
		}
		// DEQ_COUNT1(3)
		if leaf&(128>>3) != 0 {
			if int32(bsCache) < 0 {
				dst[dstIdx+3] = -one
			} else {
				dst[dstIdx+3] = one
			}
			bsCache <<= 1
			bsSh++
		}
		// CHECK_BITS
		for bsSh >= 0 {
			bsCache |= uint32(buf[bsNextPtr]) << uint(bsSh)
			bsNextPtr++
			bsSh -= 8
		}
		dstIdx += 4
	}

	bs.Pos = layer3grLimit
}
