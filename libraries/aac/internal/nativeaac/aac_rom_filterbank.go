// Narrowed FIXP_STP/FIXP_WTP tables, materialised once from the raw Q1.31 data
// by stcNarrow (== FX_DBL2FXCONST_SGL) — the exact bytes the C compiler emits
// under SINETABLE_16BIT / WINDOWTABLE_16BIT.
var (
	sineTable1024  [513]fixSTP
	sineWindow1024 [512]fixSTP
	sineWindow128  [64]fixSTP
	kbdWindow1024  [512]fixSTP
	kbdWindow128   [64]fixSTP
)

func narrowRom(dst []fixSTP, src []romPair) {
	for i := range src {
		dst[i] = fixSTP{re: stcNarrow(src[i].cos), im: stcNarrow(src[i].sin)}
	}
}

func init() {
	narrowRom(sineTable1024[:], sineTable1024Raw[:])
	narrowRom(sineWindow1024[:], sineWindow1024Raw[:])
	narrowRom(sineWindow128[:], sineWindow128Raw[:])
	narrowRom(kbdWindow1024[:], kBDWindow1024Raw[:])
	narrowRom(kbdWindow128[:], kBDWindow128Raw[:])
}
