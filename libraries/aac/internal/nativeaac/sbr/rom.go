		qmfPhaseshiftCos32[i] = nativeaac.StcNarrow(int32(qmfPhaseshiftCos32Raw[i]))
		qmfPhaseshiftSin32[i] = nativeaac.StcNarrow(int32(qmfPhaseshiftSin32Raw[i]))
